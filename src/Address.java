public class Address {
   private int number;
   private String name;
   private String city;
   private int id_address;
   private int postal_code;

    public Address(int nombre, String nom, String ville, int ID_adresse, int code_postale){
        this.number = nombre;
        this.name = nom;
        this.city = ville;
        this.id_address = ID_adresse;
        this.postal_code = code_postale;
    }

}
